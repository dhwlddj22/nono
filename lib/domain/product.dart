class Product {
  final String name;
  final String brand;
  final String shortDescription;
  final String description;
  final String price;
  final String image;
  final double rating;
  final int reviews;
  final String link;

  Product({
    required this.name,
    required this.brand,
    required this.shortDescription,
    required this.description,
    required this.price,
    required this.image,
    required this.rating,
    required this.reviews,
    required this.link,
  });
}